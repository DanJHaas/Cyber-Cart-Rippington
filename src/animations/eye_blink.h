class EyeBlink
{
  public:
    int FrameTime = 25;
    bool mirror = true;
    std::vector<int> Pos = {0, 16};
    std::vector<int> Size = {128, 32};
    std::vector<uint8_t*> Frames = {Frame1, Frame2, Frame3, Frame4};
    
    EyeBlink(){
      if(!mirror)
        return;

      for (int index = this->Frames.size() - 2; index >= 0; index--)
      {
        this->Frames.push_back(this->Frames[index]);
      }
    };

    void play()
    {
      for(auto frame : this->Frames)
      {
        display.clearBuffer();
        display.drawXBMP(this->Pos[0], this->Pos[1], this->Size[0], this->Size[1], &*frame);
        display.sendBuffer();
        delay(this->FrameTime);
      }
    };
};






